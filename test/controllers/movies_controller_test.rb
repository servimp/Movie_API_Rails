require "test_helper"

class MoviesControllerTest < ActionDispatch::IntegrationTest
  setup do
    @movie = movies(:one)
  end

  test "should get index" do
    get movies_url, as: :json
    assert_response :success
  end

  test "should create movie" do
    assert_difference("Movie.count") do
      post movies_url, params: { movie: { avg_score: @movie.avg_score, name: @movie.name, preview_video_url: @movie.preview_video_url, runtime: @movie.runtime, synopsis: @movie.synopsis } }, as: :json
    end

    assert_response :created
  end

  test "should show movie" do
    get movie_url(@movie), as: :json
    assert_response :success
  end

  test "should update movie" do
    patch movie_url(@movie), params: { movie: { avg_score: @movie.avg_score, name: @movie.name, preview_video_url: @movie.preview_video_url, runtime: @movie.runtime, synopsis: @movie.synopsis } }, as: :json
    assert_response :success
  end

  test "should destroy movie" do
    assert_difference("Movie.count", -1) do
      delete movie_url(@movie), as: :json
    end

    assert_response :no_content
  end
end
